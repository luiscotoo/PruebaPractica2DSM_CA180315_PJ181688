package sv.edu.udb.carsmotorsapp;

public class Instancias {

    private Integer id;
    private String nombre;

    public Instancias() {
        this.id = id;
        this.nombre = nombre;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}

